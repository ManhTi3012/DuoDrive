void EncoderCalc() {
    int16_t encoder1 = TIM2->CNT;
    TIM2->CNT = 0;  // Reset counter after reading

    Motor1_Pos += (double)encoder1;  // Update absolute position (double precision)

    // Convert to RPM with floating-point precision
    Motor1_Speed = ((double)encoder1 * 3000.0) / Motor1_PPR;
}


void SetPWM(){
	if(motor1_pwm == 0){
		TIM1 -> CCR1 = 0;
		TIM1 -> CCR2 = 0;
		high1.freq = 10;
		low1.freq = 10;
	}
	else if(motor1_pwm > 0){
		TIM1 -> CCR1 = 0;
		TIM1 -> CCR2 = motor1_pwm;
		high1.freq = 3;
		low1.freq = 0;
	}
	else{
		TIM1 -> CCR1 = -motor1_pwm;
		TIM1 -> CCR2 = 0;
		high1.freq = 0;
		low1.freq = 3;
	}

}
void LEDBlink(LED led){
	// set blink pattern for each led
	// 0 = off, 11 = full, 12 = no control
	// 1-10 = blink, smaller value blink faster
	switch (led.freq) {
		case 12:
			break;
		case 11:
			HAL_GPIO_WritePin(GPIOB, led.gpio, 1);
			break;
		case 0:
			HAL_GPIO_WritePin(GPIOB, led.gpio, 0);
			break;
		default:
			if(led.counter > 0){
				led.counter -= 1;
			}
			else{
				HAL_GPIO_TogglePin(GPIOB, led.gpio);
				led.counter = led.freq;
			}
			break;
	}
}

